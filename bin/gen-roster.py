#!/usr/bin/env python3
"""Generate every downstream LLM-roster config from models.yaml.

models.yaml is the single source of truth. This script regenerates:

  1. ~/Code/otel-local-ai/litellm/config.yaml      (live LiteLLM model_list)
  2. ~/Code/claude-code-config/models.json          (Pi/JSON provider format)
  3. ~/Code/hermes-dispatch/hermes-home/config.yaml (Hermes models block)
  4. ~/Code/mac-mini-llm-roster/litellm-example/config.yaml (illustrative copy)

For (1) and (3) only the generated block is replaced; all other sections
(litellm_settings, general_settings, the entire Hermes agent config, etc.) are
preserved byte-for-byte via marker-bounded splicing. (2) and (4) are emitted
in full.

Idempotent: running twice produces identical output. Use --dry-run to preview.
"""

from __future__ import annotations

import argparse
import difflib
import json
import os
import re
import sys

try:
    import yaml
except ImportError:
    sys.exit("error: PyYAML is required. Install with: python3 -m pip install --user pyyaml")

HOME = os.path.expanduser("~")
ROSTER = os.path.join(HOME, "Code/claude-code-config/models.yaml")

LITELLM_CONFIG = os.path.join(HOME, "Code/otel-local-ai/litellm/config.yaml")
MODELS_JSON = os.path.join(HOME, "Code/claude-code-config/models.json")
HERMES_CONFIG = os.path.join(HOME, "Code/hermes-dispatch/hermes-home/config.yaml")
MACMINI_CONFIG = os.path.join(HOME, "Code/mac-mini-llm-roster/litellm-example/config.yaml")

GEN_START = "# >>> GENERATED by bin/gen-roster.sh from models.yaml — DO NOT EDIT BELOW <<<"
GEN_END = "# >>> END GENERATED — do not add hand edits inside this block <<<"

PI_THINKING_DEFAULT_MAX_TOKENS = 16384


# ── loading ───────────────────────────────────────────────────────────────

def load_roster() -> dict:
    with open(ROSTER) as fh:
        return yaml.safe_load(fh)


def section_for(alias: str) -> str:
    if alias in ("fast", "balanced", "max"):
        return "tiers"
    if alias in ("structured", "code", "writing", "reasoning"):
        return "task roles"
    return "retained"


# ── LiteLLM model_list ──────────────────────────────────────────────────────

def litellm_entry(model_name: str, tag: str, num_ctx: int, think, api_base: str) -> str:
    lines = [
        f"  - model_name: {model_name}",
        "    litellm_params:",
        f"      model: ollama_chat/{tag}",
        f"      api_base: {api_base}",
        f"      num_ctx: {num_ctx}",
    ]
    if think is not None:
        lines.append(f"      think: {str(bool(think)).lower()}")
    return "\n".join(lines)


def build_model_list_inner(roster: dict) -> str:
    """The raw `model_list:` block (no markers)."""
    api_base = roster["runtime"]["proxy_to_runtime_base"]
    out = ["model_list:"]

    current_section = None
    for m in roster["models"]:
        alias = m["alias"]
        sec = section_for(alias)
        if sec != current_section:
            out.append(f"  # {sec}")
            current_section = sec
        num_ctx = m.get("litellm_num_ctx", m["context_window"])
        out.append(litellm_entry(alias, m["ollama_tag"], num_ctx, m.get("litellm_think"), api_base))

    judges = roster.get("judge_aliases") or []
    if judges:
        out.append("  # judge/gate aliases (LiteLLM-only, low context to co-load beside `max`)")
        for j in judges:
            out.append(
                litellm_entry(
                    j["alias"], j["ollama_tag"], j["litellm_num_ctx"], j.get("litellm_think"), api_base
                )
            )

    return "\n".join(out) + "\n"


def build_model_list_block(roster: dict) -> str:
    """Marker-wrapped model_list block for splicing into the live config."""
    inner = build_model_list_inner(roster)
    return (
        f"{GEN_START}\n"
        "# Edit ~/Code/claude-code-config/models.yaml and re-run bin/gen-roster.sh.\n"
        f"{inner}"
        f"{GEN_END}\n"
    )


# ── Pi / models.json ────────────────────────────────────────────────────────

def build_models_json(roster: dict) -> str:
    pi = roster["pi"]
    provider = pi["provider"]

    models = []
    for m in roster["models"]:
        if m.get("pi") is False:
            continue
        entry = {
            "id": m["alias"],
            "name": m.get("pi_name", m["role_label"]),
            "reasoning": bool(m["reasoning"]),
            "input": list(m["input"]),
            "contextWindow": m["context_window"],
            "maxTokens": m.get("pi_max_tokens", PI_THINKING_DEFAULT_MAX_TOKENS),
        }
        if m.get("pi_thinking_map"):
            entry["thinkingLevelMap"] = pi["thinking_level_map"]
        models.append(entry)

    data = {
        "_comment": pi["comment"],
        "workflows": pi["workflows"],
        "providers": {
            "litellm": {
                "baseUrl": roster["proxy"]["base_url"],
                "api": provider["api"],
                "apiKey": provider["apiKey"],
                "authHeader": provider["authHeader"],
                "compat": provider["compat"],
                "models": models,
            }
        },
    }
    return json.dumps(data, indent=2, ensure_ascii=False) + "\n"


# ── Hermes models block ─────────────────────────────────────────────────────

def build_hermes_block(roster: dict) -> str:
    hermes_models = [m for m in roster["models"] if m.get("hermes_context_length") is not None]
    width = max(len(m["alias"]) for m in hermes_models) + 1  # +1 for the colon

    lines = [
        f"  {GEN_START}",
        "  # Edit ~/Code/claude-code-config/models.yaml and re-run bin/gen-roster.sh.",
        "  models:",
    ]
    current_section = None
    for m in hermes_models:
        sec = section_for(m["alias"])
        if sec != current_section:
            lines.append(f"    # {sec}")
            current_section = sec
        label = (m["alias"] + ":").ljust(width)
        lines.append(f"    {label} {{ context_length: {m['hermes_context_length']} }}")
    lines.append(f"  {GEN_END}")
    return "\n".join(lines) + "\n"


# ── mac-mini illustrative copy ──────────────────────────────────────────────

def build_macmini(roster: dict, litellm_tail: str) -> str:
    header = (
        "# Generated from claude-code-config/models.yaml — run bin/gen-roster.sh to regenerate.\n"
        "#\n"
        "# Illustrative copy of the live LiteLLM proxy config (the live file is\n"
        "# ~/Code/otel-local-ai/litellm/config.yaml). Each alias is a config bundle —\n"
        "# model tag, context window, thinking setting — applied by the proxy so callers\n"
        "# only ever send the alias as `model`.\n"
        "\n"
    )
    inner = build_model_list_inner(roster)
    return header + inner + "\n" + litellm_tail


# ── splicing ────────────────────────────────────────────────────────────────

def splice_marked(original: str, new_block: str, first_run_anchor: str, mode: str) -> str:
    """Replace the marker-bounded region with new_block, preserving the rest.

    On a re-run the GEN_START..GEN_END markers bound the region exactly; head and
    tail outside them are preserved. On the first run (markers absent) `mode`
    decides which side is preserved:
      - "prefix": the generated block leads the file. `first_run_anchor` matches
        the first PRESERVED line; result = block + blank line + preserved tail.
      - "suffix": the generated block trails the file. `first_run_anchor` matches
        the first REPLACED line; result = preserved head + block (to EOF).
    """
    lines = original.splitlines(keepends=True)

    start_idx = end_idx = None
    for i, line in enumerate(lines):
        if GEN_START in line:
            start_idx = i
        elif GEN_END in line and start_idx is not None:
            end_idx = i
            break

    if start_idx is not None and end_idx is not None:
        head = "".join(lines[:start_idx])
        tail = "".join(lines[end_idx + 1:])
        return head + new_block + tail

    # First run: no markers yet.
    anchor_re = re.compile(first_run_anchor)
    for i, line in enumerate(lines):
        if anchor_re.match(line):
            if mode == "prefix":
                return new_block + "\n" + "".join(lines[i:])
            return "".join(lines[:i]) + new_block  # suffix
    raise RuntimeError(f"could not find first-run anchor {first_run_anchor!r}")


def extract_litellm_tail(original: str) -> str:
    """Everything from the first preserved top-level section (litellm_settings) to EOF."""
    lines = original.splitlines(keepends=True)
    for i, line in enumerate(lines):
        if re.match(r"^litellm_settings:", line):
            return "".join(lines[i:])
    raise RuntimeError("could not find litellm_settings: in otel config")


# ── output / diffing ────────────────────────────────────────────────────────

def read_or_empty(path: str) -> str:
    try:
        with open(path) as fh:
            return fh.read()
    except FileNotFoundError:
        return ""


def emit(path: str, new_content: str, dry_run: bool) -> bool:
    """Write (unless dry-run) and print a diff. Returns True if changed."""
    old = read_or_empty(path)
    rel = path.replace(HOME, "~")
    if old == new_content:
        print(f"  {rel}: no changes")
        return False

    diff = difflib.unified_diff(
        old.splitlines(keepends=True),
        new_content.splitlines(keepends=True),
        fromfile=f"{rel} (current)",
        tofile=f"{rel} (generated)",
    )
    print(f"  {rel}: CHANGED")
    for line in diff:
        sys.stdout.write("    " + line if line.endswith("\n") else "    " + line + "\n")
    print()

    if not dry_run:
        os.makedirs(os.path.dirname(path), exist_ok=True)
        with open(path, "w") as fh:
            fh.write(new_content)
    return True


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--dry-run", action="store_true", help="print diffs without writing")
    args = ap.parse_args()

    roster = load_roster()
    mode = "DRY RUN — no files written" if args.dry_run else "writing changes"
    print(f"gen-roster ({mode})\n")

    changed = False

    # 1. LiteLLM live config — splice model_list, preserve everything else.
    litellm_old = read_or_empty(LITELLM_CONFIG)
    if not litellm_old:
        sys.exit(f"error: {LITELLM_CONFIG} not found — cannot preserve non-model sections")
    block = build_model_list_block(roster)
    litellm_new = splice_marked(litellm_old, block, first_run_anchor=r"^litellm_settings:", mode="prefix")
    changed |= emit(LITELLM_CONFIG, litellm_new, args.dry_run)

    # 2. Pi / models.json — full regen.
    changed |= emit(MODELS_JSON, build_models_json(roster), args.dry_run)

    # 3. Hermes models block — splice, preserve the rest.
    hermes_old = read_or_empty(HERMES_CONFIG)
    if not hermes_old:
        sys.exit(f"error: {HERMES_CONFIG} not found — cannot preserve agent config")
    hblock = build_hermes_block(roster)
    hermes_new = splice_marked(hermes_old, hblock, first_run_anchor=r"^  models:", mode="suffix")
    changed |= emit(HERMES_CONFIG, hermes_new, args.dry_run)

    # 4. mac-mini illustrative copy — full regen using the (new) litellm tail.
    tail = extract_litellm_tail(litellm_new)
    changed |= emit(MACMINI_CONFIG, build_macmini(roster, tail), args.dry_run)

    print("done." if changed else "done — everything already up to date.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
