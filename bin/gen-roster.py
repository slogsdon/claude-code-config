#!/usr/bin/env python3
"""Generate every downstream LLM-roster config from models.yaml.

models.yaml is the single source of truth. This script regenerates:

  1. ~/Code/otel-local-ai/litellm/config.yaml      (live LiteLLM model_list)
  2. ~/Code/claude-code-config/models.json          (Pi/JSON provider format)
  3. ~/Code/hermes-dispatch/hermes-home/config.yaml (Hermes models block)
  4. ~/Code/mac-mini-llm-roster/litellm-example/config.yaml (illustrative copy)
  5. ~/.config/llama-swap/config.yaml                (llama-swap model defs)
  6. ~/.hermes/config.yaml                           (Hermes Desktop litellm context_length map — best-effort)

For (1) and (3) only the generated block is replaced; all other sections
(litellm_settings, general_settings, the entire Hermes agent config, etc.) are
preserved byte-for-byte via marker-bounded splicing. (2) and (4) are emitted
in full.

Idempotent: running twice produces identical output. Use --dry-run to preview.
"""

from __future__ import annotations

import argparse
import difflib
import json
import os
import re
import sys

try:
    import yaml
except ImportError:
    sys.exit("error: PyYAML is required. Install with: python3 -m pip install --user pyyaml")

HOME = os.path.expanduser("~")
ROSTER = os.path.join(HOME, "Code/claude-code-config/models.yaml")

LITELLM_CONFIG = os.path.join(HOME, "Code/otel-local-ai/litellm/config.yaml")
MODELS_JSON = os.path.join(HOME, "Code/claude-code-config/models.json")
HERMES_CONFIG = os.path.join(HOME, "Code/hermes-dispatch/hermes-home/config.yaml")
MACMINI_CONFIG = os.path.join(HOME, "Code/mac-mini-llm-roster/litellm-example/config.yaml")
LLAMASWAP_CONFIG = os.path.join(HOME, ".config/llama-swap/config.yaml")
# Live Hermes Desktop (Nous app) config. It discovers the litellm provider's
# model LIST live from /v1/models; this file only declares per-model
# context_length. Machine-specific — synced best-effort, skipped if absent.
HERMES_DESKTOP_CONFIG = os.path.join(HOME, ".hermes/config.yaml")

LLAMASWAP_HEALTH_TIMEOUT = 120  # big GGUFs cold-load in ~25-40s

GEN_START = "# >>> GENERATED by bin/gen-roster.sh from models.yaml — DO NOT EDIT BELOW <<<"
GEN_END = "# >>> END GENERATED — do not add hand edits inside this block <<<"

PI_THINKING_DEFAULT_MAX_TOKENS = 16384


# ── loading ───────────────────────────────────────────────────────────────

def load_roster() -> dict:
    with open(ROSTER) as fh:
        return yaml.safe_load(fh)


def section_for(alias: str) -> str:
    if alias in ("fast", "balanced", "max"):
        return "tiers"
    if alias in ("structured", "code", "writing", "reasoning"):
        return "task roles"
    return "retained"


# ── LiteLLM model_list ──────────────────────────────────────────────────────

def litellm_entry(model_name: str, think, api_base: str) -> str:
    """A LiteLLM model_list entry pointing at llama-swap (OpenAI-compatible).

    The runtime is llama-swap on :8081 — LiteLLM sends the alias as `model`
    (openai/<alias>); llama-swap's aliases block resolves it to a GGUF and
    swaps it in. Context is fixed at llama-server launch (see the llama-swap
    config), so there is no per-request num_ctx. Thinking is toggled per
    request via chat_template_kwargs.enable_thinking (replaces Ollama `think`).
    """
    lines = [
        f"  - model_name: {model_name}",
        "    litellm_params:",
        f"      model: openai/{model_name}",
        f"      api_base: {api_base}",
        "      api_key: sk-llama-swap-noauth",
    ]
    if think is False:
        lines += [
            "      extra_body:",
            "        chat_template_kwargs:",
            "          enable_thinking: false",
        ]
    return "\n".join(lines)


def cloud_litellm_entry(m: dict) -> str:
    """A LiteLLM model_list entry for an external provider (OpenRouter/NIM/etc.).

    Unlike llama-swap-backed locals, cloud entries carry the upstream provider
    slug directly and read their key from the environment. Optional OpenRouter-
    side fallback chain and per-request price ceiling are nested under extra_body.
    """
    lines = [
        f"  - model_name: {m['alias']}",
        "    litellm_params:",
        f"      model: {m['slug']}",
        f"      api_key: os.environ/{m['api_key_env']}",
    ]
    fallbacks = m.get("fallbacks") or []
    max_price = m.get("max_price")
    if fallbacks or max_price is not None:
        lines.append("      extra_body:")
        if fallbacks:
            lines.append("        models:")
            for slug in fallbacks:
                lines.append(f"          - {slug}")
        if max_price is not None:
            lines.append("        provider:")
            lines.append(f"          max_price: {{ completion: {max_price} }}")
    return "\n".join(lines)


def build_model_list_inner(roster: dict) -> str:
    """The raw `model_list:` block (no markers)."""
    api_base = roster["runtime"]["proxy_to_runtime_base"]
    out = ["model_list:"]

    current_section = None
    for m in roster["models"]:
        alias = m["alias"]
        sec = section_for(alias)
        if sec != current_section:
            out.append(f"  # {sec}")
            current_section = sec
        out.append(litellm_entry(alias, m.get("litellm_think"), api_base))

    judges = roster.get("judge_aliases") or []
    if judges:
        out.append("  # judge/gate aliases (LiteLLM-only; share a llama-swap model with `max`/`balanced`)")
        for j in judges:
            out.append(litellm_entry(j["alias"], j.get("litellm_think"), api_base))

    cloud = roster.get("cloud_models") or []
    if cloud:
        out.append("  # cloud tier (external providers; not llama-swap-backed)")
        for m in cloud:
            out.append(cloud_litellm_entry(m))

    return "\n".join(out) + "\n"


def build_model_list_block(roster: dict) -> str:
    """Marker-wrapped model_list block for splicing into the live config."""
    inner = build_model_list_inner(roster)
    return (
        f"{GEN_START}\n"
        "# Edit ~/Code/claude-code-config/models.yaml and re-run bin/gen-roster.sh.\n"
        f"{inner}"
        f"{GEN_END}\n"
    )


# ── llama-swap config ────────────────────────────────────────────────────────

def real_ctx(entry: dict) -> int:
    """The actual llama-server -c for a model: the real num_ctx, not the advertised."""
    return entry.get("litellm_num_ctx", entry.get("context_window"))


def build_llamaswap_groups(roster: dict):
    """Dedupe aliases by their runtime model id (ollama_tag).

    Several aliases share one GGUF (max/reasoning/judges -> qwen3.6:35b). They
    become ONE llama-swap model launched at the MAX context any consumer needs.
    Returns (ordered_ids, {id: {"path", "ctx"}}).
    """
    order, groups = [], {}

    def touch(tag):
        if tag not in groups:
            groups[tag] = {"path": None, "ctx": 0, "parallel": 0, "n_predict": 0, "extra_flags": "", "aliases": []}
            order.append(tag)
        return groups[tag]

    for m in roster["models"]:
        g = touch(m["ollama_tag"])
        if m.get("gguf_path"):
            g["path"] = m["gguf_path"]
        g["ctx"] = max(g["ctx"], real_ctx(m))
        g["parallel"] = max(g["parallel"], m.get("parallel", 0))
        g["n_predict"] = max(g["n_predict"], m.get("n_predict", 0))
        if m.get("extra_flags"):
            g["extra_flags"] = m["extra_flags"]
        g["aliases"].append(m["alias"])
    for j in roster.get("judge_aliases", []):
        g = touch(j["ollama_tag"])
        g["ctx"] = max(g["ctx"], j["litellm_num_ctx"])
        g["aliases"].append(j["alias"])

    missing = [t for t, g in groups.items() if not g["path"]]
    if missing:
        sys.exit(f"error: no gguf_path for llama-swap model id(s): {', '.join(missing)}")
    return order, groups


def build_llamaswap_config(roster: dict) -> str:
    rt = roster["runtime"]
    bin_path = os.path.expanduser(rt.get("llama_server_bin", "~/bin/llama-server"))
    ttl = rt.get("ttl", 300)
    order, groups = build_llamaswap_groups(roster)

    out = [
        "# >>> GENERATED by bin/gen-roster.py from models.yaml — DO NOT EDIT <<<",
        "# Edit ~/Code/claude-code-config/models.yaml and re-run bin/gen-roster.sh.",
        "#",
        "# llama-swap on-demand loads/unloads each GGUF on llama-server (llama.cpp).",
        "# LiteLLM sends an alias as `model`; the aliases: block maps it to a model id.",
        "# Aliases that share an id (max/reasoning/judges) reuse ONE resident process.",
        f"healthCheckTimeout: {LLAMASWAP_HEALTH_TIMEOUT}",
        "logLevel: info",
        "",
        "models:",
    ]
    for tag in order:
        g = groups[tag]
        path = os.path.expanduser(g["path"])
        # When `parallel` is set, -c is the TOTAL KV split across N slots, so the
        # advertised per-slot context_window is multiplied up at launch.
        n_slots = g.get("parallel") or 0
        total_ctx = g["ctx"] * n_slots if n_slots else g["ctx"]
        cmd = (
            f"{bin_path} --port ${{PORT}} --host 127.0.0.1 "
            f"--model {path} -ngl 99 -fa on -c {total_ctx} --jinja"
        )
        if n_slots:
            cmd += f" --parallel {n_slots}"
        if g.get("n_predict"):
            cmd += f" -n {g['n_predict']}"
        if g.get("extra_flags"):
            cmd += f" {g['extra_flags']}"
        out += [
            f'  "{tag}":',
            f'    cmd: "{cmd}"',
            f"    ttl: {ttl}",
            "    aliases:",
        ]
        # every LiteLLM model_name that maps to this id (the alias callers send)
        out += [f'      - "{a}"' for a in g["aliases"]]
    return "\n".join(out) + "\n"


# ── Pi / models.json ────────────────────────────────────────────────────────

def build_models_json(roster: dict) -> str:
    pi = roster["pi"]
    provider = pi["provider"]

    models = []
    for m in roster["models"]:
        if m.get("pi") is False:
            continue
        entry = {
            "id": m["alias"],
            "name": m.get("pi_name", m["role_label"]),
            "reasoning": bool(m["reasoning"]),
            "input": list(m["input"]),
            "contextWindow": m["context_window"],
            "maxTokens": m.get("pi_max_tokens", PI_THINKING_DEFAULT_MAX_TOKENS),
        }
        if m.get("pi_thinking_map"):
            entry["thinkingLevelMap"] = pi["thinking_level_map"]
        models.append(entry)

    for m in roster.get("cloud_models", []):
        if m.get("pi") is False:
            continue
        models.append({
            "id": m["alias"],
            "name": m.get("pi_name", m.get("role_label", m["alias"])),
            "reasoning": bool(m.get("reasoning", False)),
            "input": list(m.get("input", ["text"])),
            "contextWindow": m["context_window"],
            "maxTokens": m.get("pi_max_tokens", PI_THINKING_DEFAULT_MAX_TOKENS),
        })

    data = {
        "_comment": pi["comment"],
        "workflows": pi["workflows"],
        "providers": {
            "litellm": {
                "baseUrl": roster["proxy"]["base_url"],
                "api": provider["api"],
                "apiKey": provider["apiKey"],
                "authHeader": provider["authHeader"],
                "compat": provider["compat"],
                "models": models,
            }
        },
    }
    return json.dumps(data, indent=2, ensure_ascii=False) + "\n"


# ── Hermes models block ─────────────────────────────────────────────────────

def build_hermes_block(roster: dict) -> str:
    hermes_models = [m for m in roster["models"] if m.get("hermes_context_length") is not None]
    width = max(len(m["alias"]) for m in hermes_models) + 1  # +1 for the colon

    lines = [
        f"  {GEN_START}",
        "  # Edit ~/Code/claude-code-config/models.yaml and re-run bin/gen-roster.sh.",
        "  models:",
    ]
    current_section = None
    for m in hermes_models:
        sec = section_for(m["alias"])
        if sec != current_section:
            lines.append(f"    # {sec}")
            current_section = sec
        label = (m["alias"] + ":").ljust(width)
        lines.append(f"    {label} {{ context_length: {m['hermes_context_length']} }}")
    lines.append(f"  {GEN_END}")
    return "\n".join(lines) + "\n"


# ── mac-mini illustrative copy ──────────────────────────────────────────────

def build_macmini(roster: dict, litellm_tail: str) -> str:
    header = (
        "# Generated from claude-code-config/models.yaml — run bin/gen-roster.sh to regenerate.\n"
        "#\n"
        "# Illustrative copy of the live LiteLLM proxy config (the live file is\n"
        "# ~/Code/otel-local-ai/litellm/config.yaml). Each alias is a config bundle —\n"
        "# model tag, context window, thinking setting — applied by the proxy so callers\n"
        "# only ever send the alias as `model`.\n"
        "\n"
    )
    inner = build_model_list_inner(roster)
    return header + inner + "\n" + litellm_tail


# ── splicing ────────────────────────────────────────────────────────────────

def splice_marked(original: str, new_block: str, first_run_anchor: str, mode: str) -> str:
    """Replace the marker-bounded region with new_block, preserving the rest.

    On a re-run the GEN_START..GEN_END markers bound the region exactly; head and
    tail outside them are preserved. On the first run (markers absent) `mode`
    decides which side is preserved:
      - "prefix": the generated block leads the file. `first_run_anchor` matches
        the first PRESERVED line; result = block + blank line + preserved tail.
      - "suffix": the generated block trails the file. `first_run_anchor` matches
        the first REPLACED line; result = preserved head + block (to EOF).
    """
    lines = original.splitlines(keepends=True)

    start_idx = end_idx = None
    for i, line in enumerate(lines):
        if GEN_START in line:
            start_idx = i
        elif GEN_END in line and start_idx is not None:
            end_idx = i
            break

    if start_idx is not None and end_idx is not None:
        head = "".join(lines[:start_idx])
        tail = "".join(lines[end_idx + 1:])
        return head + new_block + tail

    # First run: no markers yet.
    anchor_re = re.compile(first_run_anchor)
    for i, line in enumerate(lines):
        if anchor_re.match(line):
            if mode == "prefix":
                return new_block + "\n" + "".join(lines[i:])
            return "".join(lines[:i]) + new_block  # suffix
    raise RuntimeError(f"could not find first-run anchor {first_run_anchor!r}")


def extract_litellm_tail(original: str) -> str:
    """Everything from the first preserved top-level section (litellm_settings) to EOF."""
    lines = original.splitlines(keepends=True)
    for i, line in enumerate(lines):
        if re.match(r"^litellm_settings:", line):
            return "".join(lines[i:])
    raise RuntimeError("could not find litellm_settings: in otel config")


# ── output / diffing ────────────────────────────────────────────────────────

def read_or_empty(path: str) -> str:
    try:
        with open(path) as fh:
            return fh.read()
    except FileNotFoundError:
        return ""


def emit(path: str, new_content: str, dry_run: bool) -> bool:
    """Write (unless dry-run) and print a diff. Returns True if changed."""
    old = read_or_empty(path)
    rel = path.replace(HOME, "~")
    if old == new_content:
        print(f"  {rel}: no changes")
        return False

    diff = difflib.unified_diff(
        old.splitlines(keepends=True),
        new_content.splitlines(keepends=True),
        fromfile=f"{rel} (current)",
        tofile=f"{rel} (generated)",
    )
    print(f"  {rel}: CHANGED")
    for line in diff:
        sys.stdout.write("    " + line if line.endswith("\n") else "    " + line + "\n")
    print()

    if not dry_run:
        os.makedirs(os.path.dirname(path), exist_ok=True)
        with open(path, "w") as fh:
            fh.write(new_content)
    return True


def update_hermes_desktop(roster: dict, dry_run: bool) -> bool:
    """Sync the litellm custom-provider's context_length map in the live Hermes
    Desktop config (~/.hermes/config.yaml).

    Hermes Desktop discovers the model LIST live from LiteLLM /v1/models (see
    its `/model --refresh`); this map only declares per-model context_length so
    Desktop uses the right context window instead of a default. Round-trips with
    ruamel to preserve the rest of this large, user-owned config. Skips
    gracefully (no error) if the file or a litellm custom_provider is absent, so
    the generator stays portable across machines / CI.
    """
    rel = HERMES_DESKTOP_CONFIG.replace(HOME, "~")
    if not os.path.exists(HERMES_DESKTOP_CONFIG):
        print(f"  {rel}: not present — skipped (Hermes Desktop not installed here)")
        return False
    try:
        from ruamel.yaml import YAML
        from ruamel.yaml.comments import CommentedMap
    except ImportError:
        print(f"  {rel}: ruamel.yaml not installed — skipped")
        return False

    yaml_rt = YAML()
    yaml_rt.preserve_quotes = True
    with open(HERMES_DESKTOP_CONFIG) as fh:
        data = yaml_rt.load(fh)

    providers = data.get("custom_providers") if isinstance(data, dict) else None
    prov = None
    if isinstance(providers, list):
        prov = next((p for p in providers
                     if isinstance(p, dict) and p.get("name") == "litellm"), None)
    if prov is None:
        print(f"  {rel}: no litellm custom_provider — skipped")
        return False

    # Same exposure rule as the hermes-dispatch block: any model carrying
    # hermes_context_length, in roster order.
    desired = [(m["alias"], m["hermes_context_length"])
               for m in roster["models"] if m.get("hermes_context_length") is not None]
    current = prov.get("models") or {}
    current_pairs = [(k, (v or {}).get("context_length")) for k, v in current.items()]
    if current_pairs == desired:
        print(f"  {rel}: no changes")
        return False

    cur_map = dict(current_pairs)
    des_map = dict(desired)
    added = [a for a, _ in desired if a not in cur_map]
    bumped = [a for a, cl in desired if a in cur_map and cur_map[a] != cl]
    removed = [a for a in cur_map if a not in des_map]
    print(f"  {rel}: CHANGED (custom_providers.litellm.models)")
    if added:
        print(f"    + added: {', '.join(added)}")
    if bumped:
        print(f"    ~ context_length updated: {', '.join(bumped)}")
    if removed:
        print(f"    - removed: {', '.join(removed)}")
    print()

    if not dry_run:
        new_models = CommentedMap()
        for alias, cl in desired:
            entry = CommentedMap()
            entry["context_length"] = cl
            new_models[alias] = entry
        prov["models"] = new_models
        with open(HERMES_DESKTOP_CONFIG, "w") as fh:
            yaml_rt.dump(data, fh)
    return True


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--dry-run", action="store_true", help="print diffs without writing")
    args = ap.parse_args()

    roster = load_roster()
    mode = "DRY RUN — no files written" if args.dry_run else "writing changes"
    print(f"gen-roster ({mode})\n")

    changed = False

    # 1. LiteLLM live config — splice model_list, preserve everything else.
    litellm_old = read_or_empty(LITELLM_CONFIG)
    if not litellm_old:
        sys.exit(f"error: {LITELLM_CONFIG} not found — cannot preserve non-model sections")
    block = build_model_list_block(roster)
    litellm_new = splice_marked(litellm_old, block, first_run_anchor=r"^litellm_settings:", mode="prefix")
    changed |= emit(LITELLM_CONFIG, litellm_new, args.dry_run)

    # 2. Pi / models.json — full regen.
    changed |= emit(MODELS_JSON, build_models_json(roster), args.dry_run)

    # 3. Hermes models block — splice, preserve the rest.
    hermes_old = read_or_empty(HERMES_CONFIG)
    if not hermes_old:
        sys.exit(f"error: {HERMES_CONFIG} not found — cannot preserve agent config")
    hblock = build_hermes_block(roster)
    hermes_new = splice_marked(hermes_old, hblock, first_run_anchor=r"^  models:", mode="suffix")
    changed |= emit(HERMES_CONFIG, hermes_new, args.dry_run)

    # 4. mac-mini illustrative copy — full regen using the (new) litellm tail.
    tail = extract_litellm_tail(litellm_new)
    changed |= emit(MACMINI_CONFIG, build_macmini(roster, tail), args.dry_run)

    # 5. llama-swap runtime config — full regen (we own this file entirely).
    changed |= emit(LLAMASWAP_CONFIG, build_llamaswap_config(roster), args.dry_run)

    # 6. Hermes Desktop (Nous app) live config — sync the litellm provider's
    #    context_length map. Model LIST is discovered live from /v1/models, so
    #    this only fixes context windows. Machine-specific; skipped if absent.
    changed |= update_hermes_desktop(roster, args.dry_run)

    print("done." if changed else "done — everything already up to date.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
